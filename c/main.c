    _delay_ms(2500);
    clear_buffer(buffer);

    testdrawline(buffer);
    write_buffer(buffer);
    _delay_ms(2500);
    clear_buffer(buffer);

    testdrawcircle(buffer);
    write_buffer(buffer);
    _delay_ms(2500);
    clear_buffer(buffer);

    fillcircle(buffer, 32, 32, 10, 1);
    write_buffer(buffer);
    _delay_ms(2500);
